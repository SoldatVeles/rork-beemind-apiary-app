export type TranslationKeys = typeof rawTranslations.en;

/** Deep-merge a partial translation tree onto English so missing keys fall back. */
function deepMerge<T>(base: T, overrides: unknown): T {
  if (overrides === null || overrides === undefined) return base;
  if (typeof base !== "object" || base === null) {
    return (overrides as T) ?? base;
  }
  if (typeof overrides !== "object") return base;
  const out: Record<string, unknown> = { ...(base as Record<string, unknown>) };
  const o = overrides as Record<string, unknown>;
  for (const key of Object.keys(o)) {
    const baseVal = (base as Record<string, unknown>)[key];
    const overrideVal = o[key];
    if (
      baseVal &&
      typeof baseVal === "object" &&
      !Array.isArray(baseVal) &&
      overrideVal &&
      typeof overrideVal === "object" &&
      !Array.isArray(overrideVal)
    ) {
      out[key] = deepMerge(baseVal, overrideVal);
    } else if (overrideVal !== undefined) {
      out[key] = overrideVal;
    }
  }
  return out as T;
}

